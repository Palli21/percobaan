    private void tampilRiwayat() {
        Valid.tabelKosong(tabMode);
        try {
            ps = koneksi.prepareStatement(
                "SELECT pl.tgl_periksa, pl.jam, pl.no_rawat, jpl.nm_perawatan, tl.Pemeriksaan, " +
                "dpl.nilai, tl.satuan, dpl.nilai_rujukan, dpl.keterangan " +
                "FROM periksa_lab pl " +
                "INNER JOIN reg_periksa rp ON pl.no_rawat = rp.no_rawat " +
                "INNER JOIN jns_perawatan_lab jpl ON pl.kd_jenis_prw = jpl.kd_jenis_prw " +
                "INNER JOIN detail_periksa_lab dpl ON pl.no_rawat = dpl.no_rawat AND pl.tgl_periksa = dpl.tgl_periksa AND pl.jam = dpl.jam AND pl.kd_jenis_prw = dpl.kd_jenis_prw " +
                "INNER JOIN template_laboratorium tl ON dpl.id_template = tl.id_template " +
                "WHERE rp.no_rkm_medis = ? AND pl.kategori='PK' " +
                "ORDER BY pl.tgl_periksa DESC, pl.jam DESC, jpl.nm_perawatan, tl.urut"
            );
            
            try {
                ps.setString(1, no_rkm_medis);
                rs = ps.executeQuery();
                String lastDate = ""; 
                while (rs.next()) {
                    String currentDate = rs.getString("tgl_periksa");
                    if (!lastDate.equals(currentDate) && !lastDate.isEmpty()) {
                        // Tambahkan baris pemisah dengan penanda khusus
                        tabMode.addRow(new Object[]{"SEPARATOR", "", "", "", "", "", "", "", ""}); 
                    }
                    
                    tabMode.addRow(new Object[]{
                        currentDate,
                        rs.getString("jam"),
                        rs.getString("no_rawat"),
                        rs.getString("nm_perawatan"),
                        rs.getString("Pemeriksaan"),
                        rs.getString("nilai"),
                        rs.getString("satuan"),
                        rs.getString("nilai_rujukan"),
                        rs.getString("keterangan")
                    });
                    
                    lastDate = currentDate;
                }
            } catch (SQLException e) {
                System.out.println("Notifikasi Riwayat Lab: " + e);
            } finally {
                if (rs != null) rs.close();
                if (ps != null) ps.close();
            }
        } catch (SQLException e) {
            System.out.println("Notifikasi Kueri Riwayat: " + e);
        }
    }
    
