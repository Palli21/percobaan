    /**
     * --- [MODERNISASI] ---
     * Class internal untuk mewarnai tabel dengan gaya modern.
     * Menggunakan DefaultTableCellRenderer untuk efisiensi.
     */
    public class ModernTableRenderer extends DefaultTableCellRenderer {
        private final Color ABNORMAL_COLOR = new Color(200, 0, 0); // Merah tua
        private final Color NORMAL_COLOR = Color.BLACK;
        private final Color ROW_ALT_COLOR = new Color(245, 245, 245); // Abu-abu sangat muda
        private final Color SEPARATOR_COLOR = new Color(225, 235, 255); // Biru muda
        
        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component component = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            
            // Cek untuk baris pemisah
            if (table.getValueAt(row, 0) != null && table.getValueAt(row, 0).toString().equals("SEPARATOR")) {
                component.setBackground(SEPARATOR_COLOR);
                // Mengosongkan teks pada baris pemisah
                if (component instanceof JComponent) {
                    ((JComponent) component).setBorder(BorderFactory.createEmptyBorder(0,0,0,0));
                }
                setText(""); // Tidak menampilkan teks "SEPARATOR"
                return component;
            }

            // Mengatur warna latar belakang selang-seling (zebra)
            if (!isSelected) {
                component.setBackground(row % 2 == 0 ? Color.WHITE : ROW_ALT_COLOR);
            }
            
            // Default warna teks
            component.setForeground(NORMAL_COLOR);
            
            try {
                String hasilStr = table.getValueAt(row, 5) != null ? table.getValueAt(row, 5).toString().trim() : "";
                String rujukanStr = table.getValueAt(row, 7) != null ? table.getValueAt(row, 7).toString().trim() : "";
                
                if (hasilStr.isEmpty() || rujukanStr.isEmpty() || rujukanStr.equalsIgnoreCase("normal") || rujukanStr.equalsIgnoreCase("negatif")) {
                    // Jika rujukan adalah teks standar atau hasil kosong, tidak perlu diwarnai
                } else {
                    boolean isAbnormal = checkAbnormal(hasilStr, rujukanStr);
                    if (isAbnormal) {
                        component.setForeground(ABNORMAL_COLOR);
                    }
                }
            } catch (Exception e) {
                // Abaikan jika ada error saat parsing, biarkan warna default
            }

            return component;
        }
        
        private boolean checkAbnormal(String hasil, String rujukan) {
            try {
                // 1. Cek untuk rentang numerik (e.g., "10.0 - 20.5")
                if (rujukan.contains("-")) {
                    String[] parts = rujukan.split("-");
                    if (parts.length == 2) {
                        double lowerBound = Double.parseDouble(parts[0].trim());
                        double upperBound = Double.parseDouble(parts[1].trim());
                        double nilaiHasil = Double.parseDouble(hasil);
                        return nilaiHasil < lowerBound || nilaiHasil > upperBound;
                    }
                // 2. Cek untuk batas atas (e.g., "<150")
                } else if (rujukan.startsWith("<")) {
                    double upperBound = Double.parseDouble(rujukan.replace("<", "").trim());
                    double nilaiHasil = Double.parseDouble(hasil);
                    return nilaiHasil >= upperBound;
                // 3. Cek untuk batas bawah (e.g., ">50")
                } else if (rujukan.startsWith(">")) {
                    double lowerBound = Double.parseDouble(rujukan.replace(">", "").trim());
                    double nilaiHasil = Double.parseDouble(hasil);
                    return nilaiHasil <= lowerBound;
                }
            } catch (NumberFormatException e) {
                // Jika parsing gagal, bandingkan sebagai string (case-insensitive)
                return !hasil.equalsIgnoreCase(rujukan);
            }
            // Jika tidak ada kondisi di atas, bandingkan sebagai string
            return !hasil.equalsIgnoreCase(rujukan);
        }
    }
